void loop() {
  int len = strlen(TEXT);

  // ── Intro ─────────────────────────────────────────────
  animIntro();

  // ── Affiche chaque lettre du texte ────────────────────
  for (int i = 0; i < len; i++) {
    char c = TEXT[i];
    if (c == ' ') {
      matrix.renderBitmap(fSP, 8, 13);
      delay(400);
    } else {
      // Animation d'entrée : apparition progressive
      byte (*ch)[13] = getChar(c);

      // Frame 1 : seulement lignes paires
      byte tmp[8][13] = {};
      for (int r = 0; r < 8; r += 2)
        for (int col = 0; col < 13; col++)
          tmp[r][col] = ch[r][col];
      matrix.renderBitmap(tmp, 8, 13);
      delay(80);

      // Frame 2 : lettre complète
      matrix.renderBitmap(ch, 8, 13);
      delay(600);

      // Flash de sortie
      flash();
    }

    // ── Animation milieu : après la moitié du texte ───
    if (i == len / 2 - 1) {
      animMid();
    }
  }

  // ── Outro ─────────────────────────────────────────────
  animOutro();

  delay(500);
}
